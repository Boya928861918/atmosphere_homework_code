Program main
    implicit none
    real :: I2, I3, I4, x0(8)
    real :: a2(2, 2), a3(3,2), a4(4, 2)
    integer :: i

    x0 = (/1, 2, 3, 4, 5, 6, 7, 8/)
    a2 = reshape([0.58579, 3.41421, 0.85355, 0.14645], [2, 2])
    a3 = reshape([0.41577, 2.29428, 6.28995, 0.71109, 0.27852, 0.01039], [3, 2])
    a4 = reshape([0.32255, 1.74576, 4.53662, 9.39507, 0.60315, 0.35742, 0.03889, 0.00054], [4, 2])
    do i = 1, 8
        I2 = a2(1, 2)*a2(1, 1)**(x0(i)-1) + a2(2, 2)*a3(2, 1)**(x0(i)-1)
        I3 = a3(1, 2)*a3(1, 1)**(x0(i)-1) + a3(2, 2)*a3(2, 1)**(x0(i)-1) + a3(3, 2)*a3(3, 1)&
        **(x0(i)-1)
        I4 = a4(1, 2)*a4(1, 1)**(x0(i)-1) + a4(2, 2)*a4(2, 1)**(x0(i)-1) + a4(3, 2)*a4(3, 1)&
        **(x0(i)-1) + a4(4, 2)*a4(4, 1)**(x0(i)-1)
        print*, '收敛结果：', I2
        print*, '收敛结果：', I3
        print*, '收敛结果：', I4
        print*, '------------------------------------------------'
    end do

end